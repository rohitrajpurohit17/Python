
# String with escape sequences
escape_string =" Hello!\nMy self Rohit Rajpurohit.\tI really really love watching anime.\nMy favourite one\'s are Naruto,\nBleach, AOT, so on ... I love all of them.\rReally Love watching them\b\f\\"

print(escape_string)

#  \n is used to introduce new lines.

# \t is used to introduce a tab space.
# \' is used to escape a single quote.

# \r is used to return the cursor to the beginning of the line.

# \b is used to demonstrate a backspace.

# \f is used for form feed.

# \\ is used to include a backslash.


